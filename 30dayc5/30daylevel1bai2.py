#Sử dụng for in lại 10 đến 0
print("Su dung vong lap for:")
for i in range(10, -1, -1):
    print(i)
##Sử dụng while in lại 10 đến 0
print("Su dung vong lap while:")
i = 10
while i >= 0:
    print(i)
    i = i - 1
